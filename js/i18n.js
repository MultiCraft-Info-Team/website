/* ── i18n engine ── */
const i18n = {
  currentLang: 'fr',

  t(key) {
    return TRANSLATIONS[this.currentLang][key] ?? TRANSLATIONS['fr'][key] ?? key;
  },

  setLang(lang) {
    if (!TRANSLATIONS[lang]) return;
    this.currentLang = lang;
    localStorage.setItem('ds_lang', lang);
    document.documentElement.lang = lang;
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.dataset.i18n;
      el.textContent = this.t(key);
    });
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
      const key = el.dataset.i18nHtml;
      el.innerHTML = this.t(key);
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
      el.title = this.t(el.dataset.i18nTitle);
    });
    // Update <title> and meta description
    document.title = this.t('meta.title');
    const metaDesc = document.querySelector('meta[name="description"]');
    if (metaDesc) metaDesc.content = this.t('meta.description');
    // Update lang button states
    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.classList.toggle('active', btn.dataset.lang === lang);
    });
    // Re-run typing animation for hero highlight
    if (window.restartTyping) window.restartTyping();
  },

  init() {
    const saved = localStorage.getItem('ds_lang');
    const browser = (navigator.language || '').slice(0, 2);
    const lang = saved || (browser === 'fr' ? 'fr' : 'en');
    this.setLang(lang);
  }
};
